V = 7
d = [
  [0, 2, 5, 9999, 9999, 9999, 9999],
  [2, 0, 4, 6, 10, 9999, 9999],
  [5, 4, 0, 2, 9999, 9999, 9999],
  [9999, 6, 2, 0, 9999, 1, 9999],
  [9999, 10, 9999, 9999, 0, 3, 5],
  [9999, 9999, 9999, 1, 3, 0, 9],
  [9999, 9999, 9999, 9999, 5, 9, 0],
]

def warshall_floyd():
    for k in range(V):
        for i in range(V):
            for j in range(V):
                d[i][j] = min(d[i][j], d[i][k] + d[k][j])

warshall_floyd()
print(d)
